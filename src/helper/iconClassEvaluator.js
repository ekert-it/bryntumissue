// ✅ Keep original logic
export function evaluateIconClass(iconNum) {
    if (!iconNum || iconNum < 1 || iconNum > 258) {
        return null;
    }
    return iconClassMap[iconNum] || null;
}

// ✅ Additional utility: get all icon options
export function getAllIconOptions() {
    return Object.entries(iconClassMap).map(([key, value]) => ({
        label: value,
        value: parseInt(key)
    }));
}